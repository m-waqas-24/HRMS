     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $companies = Company::where('created_by' , \Auth::user()->creatorId())->get();
        return view('system-settings.companies', compact('companies'));
    }

